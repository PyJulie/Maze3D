#include "object_renderer.h"

#include <iostream>

ObjectRenderer::ObjectRenderer(const Shader & shader){
  this->shader = shader;
  initData();
}

ObjectRenderer::~ObjectRenderer(){
  glDeleteVertexArrays(1, &this->cubeVAO);
  glDeleteVertexArrays(1, &this->lampVAO);
}



void ObjectRenderer::drawObject(Texture2D & texture, Texture2D & texShader, glm::vec3 position, glm::vec3 size, glm::vec4 color){
  this->shader.use();
  glm::mat4 model;
 
  model = glm::translate(model, position);
  model = glm::scale(model, size);

  this->shader.setMatrix4f("model", model);
  this->shader.setVector4f("extColor", color);

  glActiveTexture(GL_TEXTURE0);
  texture.bind();
  glActiveTexture(GL_TEXTURE1);
  texShader.bind();

  glBindVertexArray(cubeVAO);
    glDrawArrays(GL_TRIANGLES, 0, 36);
  glBindVertexArray(0);

  
}

void ObjectRenderer::drawLamp(glm::vec3 position, glm::vec3 size, glm::vec4 color){
  this->shader.use();
  
  glm::mat4 model;
  model = glm::translate(model, position);
  model = glm::scale(model, size);

  this->shader.setMatrix4f("model", model);
  this->shader.setVector4f("lampColor", color);

  glBindVertexArray(this->lampVAO);
    glDrawArrays(GL_TRIANGLES, 0, 36);
  glBindVertexArray(0);
}

